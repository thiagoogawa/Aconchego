import SwiftUI

struct ListingImageCarouselView: View {
    let listing: Listing
    
    var body: some View {
        TabView {
            ForEach(listing.imageURLs, id: \.self) { image in
                Image(image)
                    .resizable()
                    .scaledToFit()
                    .cornerRadius(8)
            }
        }
        .tabViewStyle(.page)
    }
}

